                    <p><em>Jill Talbot’s column, <a href="https://www.theparisreview.org/blog/columns/the-last-year/">The Last Year</a>, traces in real time the moments before her daughter leaves for college. The column ran every Friday in November, January, and March. It returns for a final month this August. </em></p>
                    <div id="attachment_146731" style="width: 1034px" class="wp-caption aligncenter"><a href="https://www.theparisreview.org/blog/wp-content/uploads/2020/08/mineral-wells-baker-hotel-hero.jpg"><img aria-describedby="caption-attachment-146731" class="size-large wp-image-146731" src="https://www.theparisreview.org/blog/wp-content/uploads/2020/08/mineral-wells-baker-hotel-hero-1024x640.jpg" alt="" width="1024" height="640" srcset="https://www.theparisreview.org/blog/wp-content/uploads/2020/08/mineral-wells-baker-hotel-hero-1024x640.jpg 1024w, https://www.theparisreview.org/blog/wp-content/uploads/2020/08/mineral-wells-baker-hotel-hero-300x187.jpg 300w, https://www.theparisreview.org/blog/wp-content/uploads/2020/08/mineral-wells-baker-hotel-hero-768x480.jpg 768w" sizes="(min-width: 62.5em) 67vw, 100vw" /></a><p id="caption-attachment-146731" class="wp-caption-text">The Baker Hotel</p></div>
                    <p>The Baker Hotel rose above the Texas trees so straight ahead we didn’t trust the turns we were told to take. I pulled off I-20, and my daughter, Indie, read directions from her phone. I took a left, away from the building that loomed like a castle in the distance. It felt as if we were going in the wrong direction, until we turned onto Oak Street. As we got closer to the fourteen-story hotel (abandoned since 1970), we leaned down to marvel at the top-floor balcony, at all those empty rooms towering over the small town of Mineral Wells, fifty miles west of Fort Worth.</p>
                    <p>The state reopened in May, and the makeshift sign that had been hanging on the side of Applebee’s (<small>OPEN TO GO</small>) came down. For forty-nine days before reopening, the state of Texas had been limited to essential businesses, and while Governor Greg Abbott declared a statewide emergency, he never issued a formal stay-at-home order. All that time, I only went to the grocery store or 7-Eleven, darting with a Pac-Man savvy down aisles, away from the maskless. Only on July 2, after the reopening of Texas proved to be a disaster, did Abbott mandate the wearing of masks.</p>
                    <p>When Indie began her senior year last fall, she was gone more than she was home—band practice and contests, game nights, working until one <small>A.M.</small> sometimes at her restaurant job, going out with friends, hanging out at their houses. I understood it was a prelude, a slow and increasing separation to prepare us for her leaving. Suddenly we were both at home, navigating a new direction.</p>
                    <p>It’s hard to tell when spring turned to summer, every day the same, and for so many of those days I wondered if Indie’s last year at home would turn out not to be the last year at all. But when her university announced in June that they would hold classes on campus in the fall, I wanted to make these very last days something we’d both remember.</p>
                    <p>We call them half-tank trips, because that’s as far as we go, half a tank. We never leave the car, and we’re back home within an hour or two. Indie prefers I surprise her with the direction and destination, so it’s only after we leave—about once a week—that I tell her where we’re headed.</p>
                    <p><span id="more-146730"></span>We live forty miles north of Dallas in a town crowded by highways, concrete, and construction. Population: 142,173. The cities we visit hover between one and five thousand, except for Mineral Wells, which is around fifteen.</p>
                    <p>This summer, we’ve gone in search of an abandoned motel just north of town (never found it); followed farmland for nineteen miles to Pilot Point, Texas, where the bank robbery scene in <em>Bonnie and Clyde</em> was filmed; toured a town nicknamed Gingerbread City for the architecture of its houses; placed silk wisteria flowers on my parents’ grave; taken a fifteen-minute drive to the largest mansion in the state; learned the histories of towns as Indie read them from her phone; parked in front a saloon built in 1873, the last stop on the Chisholm Trail before the Red River; we’ve wondered at empty train depots; pulled into the parking lots of the First United Methodist Churches in Nocona and Pilot Point and Sanger, where my grandfather served as the preacher decades ago. I took photos of all these places from the car, photos that will one day be an archive of the summer Indie left for college.</p>
                    <p>Indie and I’ve watched <em>Ghost Adventures</em> for years, following Zak Bagans and his crew through abandoned hotels, dusty mansions, and rusty prisons. One episode, “Crazy Town,” featured an investigation of the Baker Hotel and one of its ghosts, a woman who fell from the balcony in the thirties.</p>
                    <p>Opened in 1929, the Baker Hotel boasted four hundred and fifty rooms, two ballrooms, a beauty shop, a bowling alley, a gymnasium, and the first Olympic-size pool in the country. It was a high-class destination in the thirties and forties, but once the interstate system led traffic away from the city in the sixties, the hotel suffered a decline until its closure. The hotel is currently being restored. As we drove around the construction fence, Indie read that Judy Garland stayed in the hotel in 1943 and mailed a letter from the post office up the hill from the hotel.</p>
                    <p>Rolling through these quiet towns, we note storefront signs: “Please wear a mask.” “All Customers Required to Wear a Mask.” “No mask, no entrance.” And the one we saw on the front of an antique store: “Governor Abbott recommends wearing masks or keeping a 6 foot distance!!!”</p>
                    <p>What stays with us, sometimes, is not what we expected to find, but what we found instead. Beyond the town square courthouses, the landmarks and landscapes and Fleetwood Mac’s <em>Greatest Hits</em>, beyond the <em>Office Ladies</em> episode for “Casino Night,” when we learned it was John Krasinski’s first on-screen kiss (Jenna Fischer’s second), Indie and I encountered a truth we wouldn’t have known as sharply had we not turned through these small pockets of Texas: the 2020 flags promoting a second term.</p>
                    <p>Could anyone have convinced any of us a year ago of all the turns to come, of all the wrong directions or the way so many would stand up to demand the right ones? Maybe there’s never been any such thing as straight ahead.</p>
                    <p>During what turned out to be our last half-tank trip, Indie received an email from her university: she will have to quarantine for fourteen days in New York before arriving on campus. Driving west, we counted back the days from her move-in date and realized how soon we must leave.</p>
                    <p>In a few days, Indie and I will begin the cross-country trip on a road we’ve never traveled, the one that will separate us.</p>
